SELECT
	DATE_FORMAT(
		t.CREATE_TIME,
		'%Y-%c-%d %H:%i'
	) 处理时间,
	COUNT(1)
FROM
	t_td_sales_order t
WHERE
	t.CREATE_TIME >= '2020-5-8 17:00:00'
AND  t.ERP_SHOP_CODE IN (
    11502,11501,11499,176706,176704,176707,11498,11497,176705,176703,11495,11496,176701,176700,176702,11500,11494,11493,326725,326726
    )
GROUP BY
	DATE_FORMAT(
		t.CREATE_TIME,
		'%Y-%c-%d %H:%i'
	)
ORDER BY
	DATE_FORMAT(
		t.CREATE_TIME,
		'%Y-%c-%d %H:%i'
	);


