SELECT DATE_FORMAT(a.CREATE_TIME, '%Y-%c-%d %H') 过仓, COUNT(1)
FROM t_send_wms a,
     t_td_return_application_summary s
WHERE a.SHOP_ID IN (
                    11498, 11497, 176700, 176702, 11500, 11494, 176704, 326725, 176705, 176706, 176707, 11496, 11499,
                    176703, 11495, 11493, 176701, 11501, 11502, 326726
    )
  AND a.CREATE_TIME > '2019-05-25 12:00:00'
  AND a.MSG_TYPE = 'RA'
  AND a.KEY_CODE = s.RAS_CODE
  AND s.IS_DIRECT_WMS_ORDER = 1
GROUP BY DATE_FORMAT(a.CREATE_TIME, '%Y-%c-%d %H')
ORDER BY DATE_FORMAT(a.CREATE_TIME, '%Y-%c-%d %H') DESC;