       ship.memo                      '自动取消指令处理结果',
       ship.REPEATS                   '自动取消处理次数'
FROM t_td_sales_order t
         LEFT JOIN t_so_step_payment_log l ON l.TID = t.PLATFORM_ORDER_CODE
         LEFT JOIN t_ma_tb_shop_info shop ON (t.ERP_SHOP_CODE = shop.ID)
         LEFT JOIN t_ag_ship_cancel_detail ship ON t.PLATFORM_ORDER_CODE = ship.TID
